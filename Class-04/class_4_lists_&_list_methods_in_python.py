print(f"Slicing [0:5]: {my_list[0:5]}")  # all 5

print("=" * 40)

"""># Membership Check


"""

# ============================================
# 5. MEMBERSHIP CHECK
# ============================================
my_list: list[str] = ["apple", "banana",
                       "cherry", "mango",
                       "elderberry"]
