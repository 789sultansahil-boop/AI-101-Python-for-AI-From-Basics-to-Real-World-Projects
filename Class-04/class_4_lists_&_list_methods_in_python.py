"""# List Slicing
Slice index se work karta ha mean ke 0 se start ho gi value is me
"""

# ============================================
# 4. LIST SLICING
# ============================================
my_list: list[str] = ["apple", "banana",
                       "cherry", "mango",
                       "elderberry"]
