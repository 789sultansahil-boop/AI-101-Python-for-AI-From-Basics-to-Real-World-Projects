"""# Adding Item in list"""

# ============================================
# 6. ADD ITEMS
# ============================================
my_list: list[str] = ["apple", "banana",
                       "cherry", "mango",
                       "elderberry"]
# append — end mein add
my_list.append("flour")
print(f"After append: {my_list}")

# insert — specific jagah add
my_list.insert(0, "Oil")
print(f"After insert: {my_list}")

print("=" * 40)

"""# Remove Item from the list"""

# ============================================
# 7. REMOVE ITEMS
# ============================================
my_list: list[str] = ["apple", "banana",
                       "cherry", "mango",
                       "elderberry", "flour"]
# remove — naam se hatao
my_list.remove("flour")
print(f"After remove: {my_list}")

# pop — index se hatao
my_list.pop(0)
print(f"After pop(0): {my_list}")

print("=" * 40)

"""# Clear All List"""

# ============================================
# 8. CLEAR — sab kuch hatao
# ============================================
my_list: list[str] = ["apple", "banana",
                       "cherry", "mango",
                       "elderberry"]
my_list.clear()
print(f"After clear: {my_list}")  # []

print("=" * 40)

"""# Sort alphabetical order"""

# ============================================
# 9. SORT — alphabetical order
# ============================================
my_list: list[str] = ["apple", "banana",
                       "cherry", "mango",
                       "elderberry"]
my_list.sort()
print(f"After sort: {my_list}")

